/** Đối tượng là gì? 
 * - Đối tượng là những gì cầm, sờ, nắm,... được.
 * Ví dụ:
 * - Đối tượng: xe ô tô
 * - Đặc điểm: động cơ, bánh xe, bô, đèn xe, yên, lọc gió, bình xăng, cần số, cần gạt, cần phanh, cần ga,...
 * - Hành động: chạy, dừng, tăng tốc, giảm tốc, bật đèn, tắt đèn, bật còi, tắt còi,...
 * 
 * - Đặc điểm của hướng đối tượng là những gì góp phần tạo nên đối tượng đó.
 * - Hành động của hướng đối tượng là những gì đối tượng đó có thể làm được.
 * 
 * - Đối tượng trong lập trình: Thuộc tính và phương thức
 * - Class (Lớp): là một khuôn mẫu có cùng những đặc điểm để tạo ra những đối tượng
